/// The INT 10h AH=05h page stride for 80x25 color text: 4096 bytes (0x1000).
/// This is the BIOS page step (eight pages fill the 32 KB aperture), slightly
/// larger than the 4000-byte visible page so consecutive pages do not overlap
/// (RBIL INTERRUP.A:2162; the IBM VGA BIOS uses 0x1000 for all 80x25 color
/// modes, padding the 96-byte gap).
pub const VGA_TEXT_PAGE_STRIDE: usize = 4096;
/// The full 32 KB text aperture the VGA decodes at B8000-BFFFF, large enough for
/// eight 4096-byte 80x25 pages so CRTC start-address scroll and INT 10h AH=05h
/// page-flip are representable (FreeVGA crtcreg.htm 0C/0Dh; the BIOS 80x25 color
/// page size is 4096 bytes). Growing this constant grows the B8000 aperture
/// automatically: the bus mapping in `video_text_offset` derives `end` from it.
pub const VGA_TEXT_MEMORY_SIZE: usize = 32 * 1024;

#[derive(Debug, Error, PartialEq, Eq)]
pub enum VideoError {
    #[error("framebuffer dimensions must be non-zero")]
    EmptyFramebuffer,
    #[error("VGA text memory offset {offset:#x} is outside the text buffer")]
    TextMemoryOutOfBounds { offset: usize },
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Framebuffer {
    pub width: u32,
    pub height: u32,
    pub indexed_pixels: Vec<u8>,
}

impl Framebuffer {
    pub fn new_indexed8(width: u32, height: u32) -> Result<Self, VideoError> {
        if width == 0 || height == 0 {
            return Err(VideoError::EmptyFramebuffer);
        }

        Ok(Self {
            width,
            height,
            indexed_pixels: vec![0; (width * height) as usize],
        })
    }

    pub fn mode13h() -> Self {
        Self::new_indexed8(MODE13H_WIDTH, MODE13H_HEIGHT).expect("Mode 13h dimensions are non-zero")
    }
}

