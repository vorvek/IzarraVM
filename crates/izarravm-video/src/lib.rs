impl Default for Dac {
    fn default() -> Self {
        Self {
            palette: STOCK_VGA_DAC_PALETTE,
            write_index: 0,
            read_index: 0,
            write_component: 0,
            read_component: 0,
        }
    }
}

impl Dac {
    pub fn set_write_index(&mut self, index: u8) {
        self.write_index = index;
        self.write_component = 0;
    }

    pub fn set_read_index(&mut self, index: u8) {
        self.read_index = index;
        self.read_component = 0;
    }

    pub fn write_data(&mut self, value: u8) {
        self.palette[self.write_index as usize][self.write_component as usize] = value & 0x3f;
        self.write_component += 1;
        if self.write_component == 3 {
            self.write_component = 0;
            self.write_index = self.write_index.wrapping_add(1);
        }
    }

    pub fn read_data(&mut self) -> u8 {
        let value = self.palette[self.read_index as usize][self.read_component as usize];
        self.read_component += 1;
        if self.read_component == 3 {
            self.read_component = 0;
            self.read_index = self.read_index.wrapping_add(1);
        }
        value
    }

    pub fn write_index(&self) -> u8 {
        self.write_index
    }

    pub fn rgb888(&self, index: u8) -> (u8, u8, u8) {
        let [r, g, b] = self.palette[index as usize];
        (expand6(r), expand6(g), expand6(b))
    }

    /// Set one DAC entry directly, bypassing the 3C9 write flip-flop. Used by the
    /// host-locked INT 10h AH=10h services. Components are masked to 6 bits.
    pub fn set_entry(&mut self, index: u8, r: u8, g: u8, b: u8) {
        self.palette[index as usize] = [r & 0x3F, g & 0x3F, b & 0x3F];
    }

    pub fn entry(&self, index: u8) -> [u8; 3] {
        self.palette[index as usize]
    }

    /// Set `entries.len()` consecutive DAC entries starting at `start`. The index
    /// wraps modulo 256.
    pub fn set_block(&mut self, start: u8, entries: &[[u8; 3]]) {
        for (offset, &[r, g, b]) in entries.iter().enumerate() {
            let index = start.wrapping_add(offset as u8);
            self.palette[index as usize] = [r & 0x3F, g & 0x3F, b & 0x3F];
        }
    }

    /// Serialize `count` consecutive DAC entries starting at `start` as R,G,B
    /// bytes (6-bit, masked), wrapping modulo 256.
    pub fn block_bytes(&self, start: u8, count: u16) -> Vec<u8> {
        let mut out = Vec::with_capacity(count as usize * 3);
        for offset in 0..count {
            let index = start.wrapping_add(offset as u8);
            let [r, g, b] = self.palette[index as usize];
            out.push(r & 0x3F);
            out.push(g & 0x3F);
            out.push(b & 0x3F);
        }
        out
    }
}

fn expand6(component: u8) -> u8 {
    // 6-bit (0..=63) to 8-bit, replicating the high bits into the low.
    (component << 2) | (component >> 4)
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct TextCell {
    pub character: u8,
    pub attribute: u8,
}

impl Default for TextCell {
    fn default() -> Self {
        Self {
            character: b' ',
            attribute: 0x07,
        }
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct TextFrame {
    pub columns: usize,
    pub rows: usize,
    pub cells: Vec<TextCell>,
    pub cursor_offset: u16,
}

impl TextFrame {
    pub fn line_string(&self, row: usize) -> String {
        let start = row * self.columns;
        let end = start + self.columns;
        self.cells[start..end]
            .iter()
            .map(|cell| match cell.character {
                0 => ' ',
                byte => char::from(byte),
            })
            .collect::<String>()
            .trim_end()
            .to_owned()
    }

    pub fn as_text(&self) -> String {
        (0..self.rows)
            .map(|row| self.line_string(row))
            .collect::<Vec<_>>()
            .join("\n")
    }
}

